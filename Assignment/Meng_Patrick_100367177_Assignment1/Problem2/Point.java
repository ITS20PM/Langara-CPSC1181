public class Point {
	
	private double x;
	private double y;
	
	//code the default and constructor using fields and point class
	Point(double x, double y){
		this.x = x;
		this.y = y;
	}

	//code the setters and getters for the point class
	public double getX() {
		return x;
	}

	public void setX(double x) {
		this.x = x;
	}

	public double getY() {
		return y;
	}

	public void setY(double y) {
		this.y = y;
	}
	
	
}
